#include "../includes/libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*new;
	size_t	i;

	i = 0;
	new = str;
	while (i < n)
	{
		new[i] = c;
		i++;
	}
	return (new);
}
