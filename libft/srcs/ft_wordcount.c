#include "../includes/libft.h"

int	ft_wordcount(char const *s, char c)
{
	int		wc;
	int		i;

	i = 0;
	wc = 0;
	while (s && *(s + i))
	{
		while (s && *(s + i) == c)
			i++;
		if (*(s + i))
			wc++;
		while (*(s + i) && *(s + i) != c)
			i++;
	}
	return (wc);
}
